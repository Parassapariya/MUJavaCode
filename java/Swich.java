public class Swich {
    public static void main(String[] args) {
        int a = 1;
        switch (a) {
            case 1:
                System.err.println("Monday");
                break;
            case 2:
                System.err.println("Tuesday");
                break;
            case 3:
                System.out.println("wednesday");
                break;
            case 4:
                System.out.println("thursday");
                break;
            case 5:
                System.err.println("friday");
                break;
            case 6:
                System.out.println("saturday");
                break;
            default:
                break;
        }
    }
}
